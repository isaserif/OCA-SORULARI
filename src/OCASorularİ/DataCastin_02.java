package OCASorularİ;

public class DataCastin_02 {
    public static void main(String[] args) {
        /*
        Yandaki kod bloğunda output nedir.
         */
        byte a=40, b=50;
        byte sum= (byte) (a+b);
        // casting yapmamız gerekir toplama işleminin sonucunu
        // byte cast edersek kod çalışır.
        System.out.println("a+b= "+sum);


    }
}
